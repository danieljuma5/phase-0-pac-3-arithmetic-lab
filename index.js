const p1 = 30
const p2 = 40

function add(p1,p2) {
  return p1 + p2;
}

function subtract(p1,p2) {
  return p1 - p2;
}

function multiply(p1,p2) {
  return p1 * p2;
}

function divide(p1,p2) {
  return p1/p2;
}


function increment(p1) {
return p1 + 1;
}
function decrement(p1) {
  return p1 - 1;
}

